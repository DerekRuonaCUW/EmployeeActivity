package com.example.employeeactivity;

public class Employee
{
    //fields
    String firstName;
    String lastName;
    int age;
    int height;
    int weight;



    public Employee(String firstName, String lastName, int age, int height, int weight)
    {
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.height = height;
        this.weight = weight;
    }


}
